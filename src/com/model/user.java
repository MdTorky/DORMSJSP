package com.model;

public class user {

    int userId;
    String userFullName;
    String userEmail;
    String userPassword;
    String userType;
    String userPassportNo;
    String userNationality;
    String userPhoneNo;
    String userProfileImage;
    String userRoomNo;
    
    public int getUserId() {
        return userId;
    }
    public void setUserId(int userId) {
        this.userId = userId;
    }
    public String getUserFullName() {
        return userFullName;
    }
    public void setUserFullName(String userFullName) {
        this.userFullName = userFullName;
    }
    public String getUserEmail() {
        return userEmail;
    }
    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }
    public String getUserPassword() {
        return userPassword;
    }
    public void setUserPassword(String userPassword) {
        this.userPassword = userPassword;
    }
    public String getUserType() {
        return userType;
    }
    public void setUserType(String userType) {
        this.userType = userType;
    }
    public String getUserPassportNo() {
        return userPassportNo;
    }
    public void setUserPassportNo(String userPassportNo) {
        this.userPassportNo = userPassportNo;
    }
    public String getUserNationality() {
        return userNationality;
    }
    public void setUserNationality(String userNationality) {
        this.userNationality = userNationality;
    }
    public String getUserPhoneNo() {
        return userPhoneNo;
    }
    public void setUserPhoneNo(String userPhoneNo) {
        this.userPhoneNo = userPhoneNo;
    }
    public String getUserProfileImage() {
        return userProfileImage;
    }
    public void setUserProfileImage(String userProfileImage) {
        this.userProfileImage = userProfileImage;
    }
    public String getUserRoomNo() {
        return userRoomNo;
    }
    public void setUserRoomNo(String userRoomNo) {
        this.userRoomNo = userRoomNo;
    }

    
}
